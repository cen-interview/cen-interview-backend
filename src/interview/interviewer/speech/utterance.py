"""면접관 발화에 사용하는 기본 안내 문장 템플릿.

이 모듈의 함수들은 외부 API, 환경변수, 세션 상태에 의존하지 않는다. 이후
LLM을 이용해 안내 문장을 생성하더라도 호출이 실패하면 이 템플릿을 최종
폴백으로 사용한다. Strategy가 생성한 질문 본문은 이 모듈에서 다루지 않고,
질문 앞에 붙일 짧은 안내 문장만 반환한다.
"""


def greeting() -> str:
    """면접 시작 인사를 반환한다.

    첫 번째 질문을 전달하기 전에 지원자에게 면접 시작을 알릴 때 사용한다.
    질문 본문은 포함하지 않으며, 이후 발화 조립 단계에서 첫 질문과 합친다.

    Returns:
        면접 시작을 알리는 기본 안내 문장.
    """
    return "안녕하세요. 지금부터 면접을 시작하겠습니다."


def question() -> str:
    """일반적인 답변 수신 리액션을 반환한다.

    새로운 메인 질문을 전달하기 전에 직전 답변을 들었다는 사실만 짧게
    표현한다. 다음 질문을 예고하거나 질문 내용을 요약하지 않는다.

    Returns:
        직전 답변에 대한 중립적인 기본 리액션.
    """
    return "네, 답변을 들었습니다."


def sufficient() -> str:
    """충분한 답변 뒤에 사용할 절제된 긍정 리액션을 반환한다.

    점수나 정답 여부를 직접 공개하지 않으면서도 답변의 핵심이 분명하게
    전달되었다는 인상을 준다. Strategy가 만든 다음 질문 본문은 포함하지 않는다.

    Returns:
        충분한 답변에 대응하는 긍정 리액션.
    """
    return "핵심을 분명하게 설명해 주셨습니다."


def bonus_available() -> str:
    """좋은 답변에서 추가 깊이를 확인할 때 사용할 리액션을 반환한다.

    기본 방향이 타당하다는 점은 알려주되 완결된 정답이라고 단정하지 않는다.
    지원자가 자신의 답변을 더 확장할 수 있다는 정도만 간접적으로 전달한다.

    Returns:
        긍정적인 답변에 추가 설명 여지가 있음을 나타내는 리액션.
    """
    return "답변의 흐름은 좋았고, 설명을 더 확장할 여지가 있겠습니다."


def confirm_positive() -> str:
    """대체로 타당하지만 설명 범위를 확인해야 할 때의 리액션을 반환한다.

    답변 전체를 부정하지 않으면서 범위나 조건이 아직 선명하지 않다는 점을
    간접적으로 전달한다. 확인할 구체적인 대상은 뒤따르는 질문에 맡긴다.

    Returns:
        설명 범위를 더 분명히 해야 함을 나타내는 리액션.
    """
    return "전반적인 방향은 맞지만, 설명의 범위를 조금 더 분명히 할 필요가 있겠습니다."


def trap_available() -> str:
    """유사 개념의 구분 기준을 확인할 때 사용할 리액션을 반환한다.

    어떤 개념을 구분해야 하는지 먼저 알려주지 않고, 답변에서 구분 기준이
    중요하다는 인상만 제공하여 뒤따르는 함정 질문의 평가 기능을 보존한다.

    Returns:
        개념 구분 기준을 확인할 필요가 있음을 나타내는 리액션.
    """
    return "말씀하신 내용에서는 개념을 구분하는 기준이 중요해 보입니다."


def misconception() -> str:
    """오개념 가능성이 있는 답변 뒤에 사용할 리액션을 반환한다.

    지원자의 답변을 틀렸다고 직접 단정하거나 정답을 알려주지 않는다. 대신
    전제와 개념 사이 관계를 다시 검토해야 한다는 신호를 분명하게 전달한다.

    Returns:
        전제 또는 개념 관계를 재검토해야 함을 나타내는 리액션.
    """
    return "방금 답변은 전제와 개념 사이의 관계를 다시 짚어볼 필요가 있겠습니다."


def confirm_negative() -> str:
    """근거나 이전 설명과 충돌할 가능성이 있을 때의 리액션을 반환한다.

    구체적인 충돌 내용이나 평가 결론은 공개하지 않고, 지원자가 자신의 설명에서
    근거와 일관성을 다시 살펴볼 수 있도록 간접적인 신호만 제공한다.

    Returns:
        답변의 근거와 일관성을 재검토해야 함을 나타내는 리액션.
    """
    return "앞서 말씀하신 내용과 함께 보면 근거의 일관성을 다시 살펴볼 필요가 있겠습니다."


def follow_up() -> str:
    """꼬리 질문 전에 사용할 짧은 답변 리액션을 반환한다.

    지원자의 직전 답변을 평가하거나 다음 질문을 예고하지 않고, 답변을
    들었다는 사실만 표현해 꼬리 질문으로 자연스럽게 이어지게 한다.

    Returns:
        꼬리 질문 앞에 붙이는 중립적인 기본 리액션.
    """
    return "말씀하신 답변에는 조금 더 깊이 살펴볼 여지가 있겠습니다."


def challenge() -> str:
    """압박 질문 전에 사용할 단호한 답변 리액션을 반환한다.

    challenge 또는 부정 확인 질문을 전달할 때 사용한다. 정답이나 평가 결과를
    직접 밝히지 않되 답변의 전제나 논리를 재검토할 필요가 있음을 전달한다.

    Returns:
        압박 또는 부정 확인 질문 앞에 붙이는 단호한 기본 리액션.
    """
    return "방금 답변은 전제와 근거를 다시 짚어볼 필요가 있겠습니다."


def hint() -> str:
    """답변 방향을 생각할 수 있도록 힌트 안내를 반환한다.

    지원자가 답변을 이어가기 어려워할 때 정답을 직접 알려주지 않고 생각할
    방향을 제시하는 질문 앞에 붙인다.

    Returns:
        힌트 질문 앞에 붙이는 기본 안내 문장.
    """
    return "답변이 어려우시다면 다음 관점에서 생각해 보셔도 좋습니다."


def replay() -> str:
    """현재 질문을 다시 전달하는 안내를 반환한다.

    지원자가 질문을 다시 요청했거나 현재 질문을 재제시해야 할 때 사용한다.
    질문을 새로 만들지 않고 발화 조립 단계에서 기존 Question.text를 덧붙인다.

    Returns:
        질문 재제시 전에 사용하는 기본 안내 문장.
    """
    return "네, 질문을 다시 말씀드리겠습니다."


def pause_prompt() -> str:
    """면접을 잠시 멈출지 확인하는 안내를 반환한다.

    장시간 응답이 없거나 지원자에게 휴식 여부를 확인해야 할 때 사용한다.
    일반 질문과 달리 이 문장 자체가 사용자에게 선택을 요청하는 발화다.

    Returns:
        면접 일시 정지 여부를 묻는 기본 안내 문장.
    """
    return "잠시 쉬었다가 면접을 계속 진행하시겠습니까?"


def closing() -> str:
    """면접 종료 안내를 반환한다.

    모든 질문을 마쳤거나 종료 요청을 처리한 뒤 마지막 발화로 사용한다. 현재
    질문 본문은 덧붙이지 않고 종료 안내만 사용자에게 전달한다.

    Returns:
        면접 종료와 감사 인사를 담은 기본 안내 문장.
    """
    return "이상으로 면접을 마치겠습니다. 참여해 주셔서 감사합니다."
