"""면접관 발화에 사용하는 기본 안내 문장 템플릿.

이 모듈의 함수들은 외부 API, 환경변수, 세션 상태에 의존하지 않는다. 이후
LLM을 이용해 안내 문장을 생성하더라도 호출이 실패하면 이 템플릿을 최종
폴백으로 사용한다. Strategy가 생성한 질문 본문은 이 모듈에서 다루지 않고,
질문 앞에 붙일 짧은 안내 문장만 반환한다.
"""


def greeting() -> str:
    """면접 시작 인사를 반환한다.

    첫 번째 질문을 전달하기 전에 지원자에게 면접 시작을 알릴 때 사용한다.
    질문 본문은 포함하지 않으며, 이후 발화 조립 단계에서 첫 질문과 합친다.

    Returns:
        면접 시작을 알리는 기본 안내 문장.
    """
    return "안녕하세요. 지금부터 면접을 시작하겠습니다."


def question() -> str:
    """일반적인 다음 질문 안내를 반환한다.

    새로운 메인 질문을 전달하기 전에 사용하는 기본 안내 문장이다. 실제
    질문 내용은 포함하지 않고 발화 조립 단계에서 Question.text를 덧붙인다.

    Returns:
        다음 메인 질문을 안내하는 기본 문장.
    """
    return "좋습니다. 다음 질문을 드리겠습니다."


def follow_up() -> str:
    """답변을 조금 더 자세히 확인하는 안내를 반환한다.

    지원자의 직전 답변에서 추가 설명이 필요한 부분을 꼬리 질문으로 확인할
    때 사용한다. 평가하거나 단정하는 표현 없이 설명을 자연스럽게 이어간다.

    Returns:
        꼬리 질문 앞에 붙이는 기본 안내 문장.
    """
    return "말씀해 주신 내용에서 한 가지를 조금 더 여쭤보겠습니다."


def challenge() -> str:
    """오개념이나 논리적 허점을 재확인하는 안내를 반환한다.

    challenge 또는 부정 확인 질문을 전달할 때 사용한다. 지원자를 공격하거나
    정답을 노출하지 않으면서도 앞선 답변을 다시 검토하도록 단호하게 안내한다.

    Returns:
        압박 또는 부정 확인 질문 앞에 붙이는 기본 안내 문장.
    """
    return "말씀하신 내용을 정확히 확인하기 위해 한 가지 더 질문드리겠습니다."


def hint() -> str:
    """답변 방향을 생각할 수 있도록 힌트 안내를 반환한다.

    지원자가 답변을 이어가기 어려워할 때 정답을 직접 알려주지 않고 생각할
    방향을 제시하는 질문 앞에 붙인다.

    Returns:
        힌트 질문 앞에 붙이는 기본 안내 문장.
    """
    return "답변이 어려우시다면 다음 관점에서 생각해 보셔도 좋습니다."


def replay() -> str:
    """현재 질문을 다시 전달하는 안내를 반환한다.

    지원자가 질문을 다시 요청했거나 현재 질문을 재제시해야 할 때 사용한다.
    질문을 새로 만들지 않고 발화 조립 단계에서 기존 Question.text를 덧붙인다.

    Returns:
        질문 재제시 전에 사용하는 기본 안내 문장.
    """
    return "네, 질문을 다시 말씀드리겠습니다."


def pause_prompt() -> str:
    """면접을 잠시 멈출지 확인하는 안내를 반환한다.

    장시간 응답이 없거나 지원자에게 휴식 여부를 확인해야 할 때 사용한다.
    일반 질문과 달리 이 문장 자체가 사용자에게 선택을 요청하는 발화다.

    Returns:
        면접 일시 정지 여부를 묻는 기본 안내 문장.
    """
    return "잠시 쉬었다가 면접을 계속 진행하시겠습니까?"


def closing() -> str:
    """면접 종료 안내를 반환한다.

    모든 질문을 마쳤거나 종료 요청을 처리한 뒤 마지막 발화로 사용한다. 현재
    질문 본문은 덧붙이지 않고 종료 안내만 사용자에게 전달한다.

    Returns:
        면접 종료와 감사 인사를 담은 기본 안내 문장.
    """
    return "이상으로 면접을 마치겠습니다. 참여해 주셔서 감사합니다."
